public enum DeviceType {

    /**
     * Represents a normal device. i.e. a browser on a desktop or laptop computer
     */
    NORMAL,

    /**
     * Represents a mobile device, such as an iPhone
     */
    MOBILE,

    /**
     * Represents a tablet device, such as an iPad
     */
    TABLET
}
